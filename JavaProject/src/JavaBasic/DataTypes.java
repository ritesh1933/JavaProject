package JavaBasic;

public class DataTypes {

	//main() method-->starting/execution point of the program
	public static void main(String[] args) {
	
		
		// PRIMITIVE DATA TYPE: int, double, char, string
		
		//1. int: 
		int i =10;
		int j =20;
		System.out.println(j);
		int k = 0;
		int l = -1;
		
		//2. Double:
		double d= 34.22;
		double d2= 100; //100.00
		
		//3. Char: single digit value
		char c = 'a';
		char c1 = '1';
		char c3 = 'A';
		char c4= '$';;
		System.out.println(c4);
		
		//4. Boolean:
		boolean b1= true;
		boolean b2= false;
		
	
		
		//5. String: it is a class, not a data type
		String s= "hello world";
		String s2= "this is a code";
		String s3= "1000";
		String s4= "33.44";
		
		System.out.println(s + " "+ "plus " + s2);
		System.out.println(s3 + s4);
		
	}

}
